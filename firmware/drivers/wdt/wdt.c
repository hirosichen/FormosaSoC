/**
 * @file wdt.c
 * @brief FormosaSoC 看門狗計時器驅動程式實作
 *
 * 設計理念：
 *   看門狗驅動程式提供系統可靠性保障，確保軟體異常時
 *   能自動恢復系統運作。
 *
 *   鎖定保護機制：
 *     看門狗暫存器預設處於鎖定狀態，修改前必須：
 *       1. 寫入解鎖鍵值 (0x1ACCE551) 至 WDT_LOCK 暫存器
 *       2. 進行暫存器修改操作
 *       3. 寫入任意非解鎖鍵值重新鎖定
 *     此機制防止軟體錯誤意外修改看門狗設定。
 *
 *   逾時計算：
 *     看門狗使用 APB 時脈（40MHz），載入值計算：
 *       load = timeout_ms * (APB_CLOCK / 1000)
 *
 * @author FormosaSoC Team
 * @date 2026-03-03
 */

#include "wdt.h"

/* =========================================================================
 *  模組內部變數
 * ========================================================================= */

/* 儲存看門狗載入值，用於 wdt_feed() 重載 */
static uint32_t wdt_reload_value = 0;

/* =========================================================================
 *  內部輔助函式：解鎖/鎖定看門狗暫存器
 * ========================================================================= */

/**
 * 解鎖看門狗暫存器
 * 說明：寫入解鎖鍵值，允許修改看門狗暫存器。
 */
static inline void wdt_unlock(void)
{
    WDT_LOCK = WDT_UNLOCK_KEY;
}

/**
 * 鎖定看門狗暫存器
 * 說明：寫入任意非解鎖鍵值，鎖定看門狗暫存器。
 */
static inline void wdt_lock(void)
{
    WDT_LOCK = 0;  /* 任意非解鎖鍵值 */
}

/* =========================================================================
 *  wdt_init() - 初始化看門狗計時器
 *  實作說明：
 *    1. 計算載入值
 *    2. 解鎖暫存器
 *    3. 設定載入值
 *    4. 設定控制暫存器（致能重設功能）
 *    5. 重新鎖定暫存器
 *
 *    初始化後看門狗尚未致能，需呼叫 wdt_enable() 啟動。
 * ========================================================================= */
formosa_status_t wdt_init(uint32_t timeout_ms)
{
    if (timeout_ms == 0) {
        return FORMOSA_INVALID;
    }

    /* 致能看門狗時脈 */
    CLKCTRL_CLK_EN |= CLKCTRL_CLK_EN_WDT_Msk;

    /* 計算載入值
     * load = timeout_ms * (APB_CLOCK / 1000)
     * 使用 uint64_t 防止乘法溢位 */
    wdt_reload_value = (uint32_t)(
        ((uint64_t)timeout_ms * FORMOSA_APB_CLOCK_HZ) / 1000ULL
    );

    if (wdt_reload_value == 0) {
        wdt_reload_value = 1;
    }

    /* 解鎖暫存器 */
    wdt_unlock();

    /* 設定載入值 */
    WDT_LOAD = wdt_reload_value;

    /* 設定控制暫存器：致能重設功能（但尚未致能計數器）
     * 逾時後將觸發系統重設 */
    WDT_CTRL = WDT_CTRL_RST_EN_Msk;

    /* 重新鎖定暫存器 */
    wdt_lock();

    return FORMOSA_OK;
}

/* =========================================================================
 *  wdt_feed() - 餵狗
 *  實作說明：
 *    將儲存的載入值重新寫入載入暫存器，重置計數器。
 *    此操作需要解鎖保護。
 *
 *    在中斷上下文中呼叫此函式是安全的。
 *    建議在主迴圈中定期呼叫，或在關鍵任務完成後呼叫。
 * ========================================================================= */
void wdt_feed(void)
{
    /* 解鎖暫存器 */
    wdt_unlock();

    /* 重新載入計數值 */
    WDT_LOAD = wdt_reload_value;

    /* 清除可能的中斷狀態 */
    WDT_INT_CLR_REG = 1;

    /* 重新鎖定暫存器 */
    wdt_lock();
}

/* =========================================================================
 *  wdt_enable() - 致能看門狗
 *  實作說明：
 *    設定控制暫存器的致能位元，開始計數。
 *    致能後必須定期呼叫 wdt_feed()，否則將觸發系統重設。
 * ========================================================================= */
void wdt_enable(void)
{
    wdt_unlock();

    /* 致能看門狗：計數致能 + 重設致能 */
    WDT_CTRL = WDT_CTRL_EN_Msk | WDT_CTRL_RST_EN_Msk;

    /* 載入初始值 */
    WDT_LOAD = wdt_reload_value;

    wdt_lock();
}

/* =========================================================================
 *  wdt_disable() - 停用看門狗
 *  實作說明：
 *    清除控制暫存器的致能位元，停止計數。
 *    需要解鎖保護。
 *
 *    警告：正式產品韌體中不應停用看門狗。
 *    此函式主要用於開發除錯階段。
 * ========================================================================= */
void wdt_disable(void)
{
    wdt_unlock();

    /* 停用看門狗 */
    WDT_CTRL = 0;

    /* 清除中斷狀態 */
    WDT_INT_CLR_REG = 1;

    wdt_lock();
}
