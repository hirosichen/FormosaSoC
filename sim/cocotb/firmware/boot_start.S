# FormosaSoC 最小啟動碼
# =============================================================================
# 設定堆疊指標 (SP) 指向 SRAM 頂端，然後跳轉到 main()。
# SRAM 位址範圍: 0x1000_0000 ~ 0x1000_FFFF (64KB)
# SP 初始值: 0x10010000 (SRAM 頂端)
# =============================================================================

    .section .text.start
    .global _start
    .type _start, @function

_start:
    # 設定堆疊指標
    lui     sp, 0x10010         # sp = 0x10010000 (SRAM top)

    # 清除 BSS (簡化版 — 跳過)

    # 跳轉到 main
    jal     ra, main

    # main 回傳後進入無限迴圈
_halt:
    j       _halt

    .size _start, . - _start
