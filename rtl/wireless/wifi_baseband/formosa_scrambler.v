// ===========================================================================
// 檔案名稱: formosa_scrambler.v
// 專案名稱: FormosaSoC - 台灣自主 IoT SoC
// 模組名稱: formosa_scrambler
// 功能描述: 資料擾碼器/解擾碼器
//           - 127 位元 LFSR（生成多項式：x^7 + x^4 + 1）
//           - 種子初始化
//           - 旁路模式
//           - 擾碼與解擾碼使用相同電路（自同步）
// 標準依據: IEEE 802.11a/g Section 17.3.5.4
// 作者:     FormosaSoC 開發團隊
// ===========================================================================
//
// 擾碼器結構：
//   LFSR 移位暫存器 S0~S6，生成多項式 G(x) = x^7 + x^4 + 1
//   回授 = S6 XOR S3（對應 x^7 和 x^4 的係數）
//   輸出 = 輸入資料 XOR 回授
//
//   ┌───┬───┬───┬───┬───┬───┬───┐
//   │S0 │S1 │S2 │S3 │S4 │S5 │S6 │
//   └─┬─┴───┴───┴─┬─┴───┴───┴─┬─┘
//     │            │            │
//     │            └──XOR───────┘
//     │               │
//     └───────────────┤
//                     │
//         data_in ──XOR── data_out
//
// ===========================================================================

`timescale 1ns / 1ps

module formosa_scrambler (
    // -----------------------------------------------------------------------
    // 系統時脈與重置
    // -----------------------------------------------------------------------
    input  wire       clk,          // 系統時脈
    input  wire       rst_n,        // 非同步重置（低電位有效）

    // -----------------------------------------------------------------------
    // 控制信號
    // -----------------------------------------------------------------------
    input  wire [6:0] seed,         // LFSR 種子值（7 位元，不可全零）
    input  wire       seed_load,    // 種子載入（高電位有效，脈衝）
    input  wire       bypass,       // 旁路模式（高電位：擾碼器透通）

    // -----------------------------------------------------------------------
    // 資料介面（位元組處理）
    // -----------------------------------------------------------------------
    input  wire [7:0] data_in,      // 輸入資料（8 位元）
    input  wire       in_valid,     // 輸入有效
    output wire       in_ready,     // 就緒可接收

    output reg  [7:0] data_out,     // 輸出資料（擾碼/解擾碼後）
    output reg        out_valid     // 輸出有效
);

    // =======================================================================
    // LFSR 暫存器（7 位元）
    // =======================================================================
    reg [6:0] lfsr;           // 移位暫存器 S[6:0]
    reg [6:0] lfsr_next;      // 下一拍的 LFSR 值

    // =======================================================================
    // 處理狀態
    // =======================================================================
    reg       busy;           // 處理中旗標
    reg [2:0] bit_cnt;        // 位元處理計數器（0~7）
    reg [7:0] shift_in;       // 輸入移位暫存器
    reg [7:0] shift_out;      // 輸出移位暫存器

    // =======================================================================
    // 就緒信號：非忙碌時可接收新資料
    // =======================================================================
    assign in_ready = !busy;

    // =======================================================================
    // LFSR 回授計算（並行 8 位元處理）
    // 生成多項式：x^7 + x^4 + 1
    // 回授位元 = S[6] XOR S[3]
    // =======================================================================

    // 單一位元的 LFSR 步進函數
    function [7:0] scramble_byte;
        input [6:0] lfsr_state;
        input [7:0] din;
        reg [6:0] s;
        reg       fb;  // 回授位元
        reg [7:0] dout;
        integer b;
        begin
            s = lfsr_state;
            for (b = 0; b < 8; b = b + 1) begin
                fb = s[6] ^ s[3];        // 回授 = S6 XOR S3
                dout[b] = din[b] ^ fb;   // 輸出 = 輸入 XOR 回授
                s = {s[5:0], fb};         // LFSR 左移，回授進入 S0
            end
            scramble_byte = dout;
        end
    endfunction

    // 計算 8 步之後的 LFSR 狀態
    function [6:0] lfsr_advance_8;
        input [6:0] lfsr_state;
        reg [6:0] s;
        reg       fb;
        integer b;
        begin
            s = lfsr_state;
            for (b = 0; b < 8; b = b + 1) begin
                fb = s[6] ^ s[3];
                s = {s[5:0], fb};
            end
            lfsr_advance_8 = s;
        end
    endfunction

    // =======================================================================
    // 主邏輯：擾碼/解擾碼處理
    // =======================================================================
    always @(posedge clk or negedge rst_n) begin
        if (!rst_n) begin
            lfsr      <= 7'b111_1111;   // 預設種子（全 1）
            data_out  <= 8'd0;
            out_valid <= 1'b0;
            busy      <= 1'b0;
        end else if (seed_load) begin
            // 載入新種子（種子不可為全零，否則 LFSR 會鎖住）
            lfsr      <= (seed == 7'd0) ? 7'b111_1111 : seed;
            out_valid <= 1'b0;
            busy      <= 1'b0;
        end else if (in_valid && !busy) begin
            if (bypass) begin
                // 旁路模式：直接透通
                data_out  <= data_in;
                out_valid <= 1'b1;
            end else begin
                // 擾碼/解擾碼：使用並行計算（單週期完成 8 位元）
                data_out  <= scramble_byte(lfsr, data_in);
                lfsr      <= lfsr_advance_8(lfsr);
                out_valid <= 1'b1;
            end
        end else begin
            out_valid <= 1'b0;
        end
    end

endmodule
